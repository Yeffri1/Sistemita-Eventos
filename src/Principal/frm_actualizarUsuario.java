    public static void rrellenarCmb()
    {
        try
        {
          Statement stm = Modelo.Conexion.conectar().createStatement();
          ResultSet rs = stm.executeQuery("Select * From Personas join Usuarios"
                  + " on Personas.IdPersonas=Usuarios.IdPersona");
          
          while(rs.next())
          {
              cmbuser_update1.addItem(rs.getString("Usuario"));
          }
          stm.close();
          rs.close();
        }
        catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null,ex.getMessage());  
        }
    }

