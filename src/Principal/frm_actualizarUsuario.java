    private void lblguardar2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblguardar2MouseClicked
        
        int Id =0;
        
        
          try
        {
          Statement stm = Modelo.Conexion.conectar().createStatement();
          ResultSet rs = stm.executeQuery("Select IdPersonas \n" +
"From Usuarios INNER JOIN Personas on Personas.IdPersonas=Usuarios.IdPersona \n" +
"  WHERE Usuario='"+cmbuser_update1.getSelectedItem().toString()+"'");
          
          
          while(rs.next())
          {
              Id=rs.getInt("IdPersonas");
          }
          
        }
          catch(SQLException ex)
          {
              JOptionPane.showMessageDialog(null,ex.getMessage()); 
          }
        Usuarios user = new Usuarios();
         Administrador admin = new Administrador();
        
        if(cmbRol.getSelectedIndex()==0)
        {
            Portero p = new Portero();
            admin._mantenimientoPersona = new Modelo.mantenimientoPortero();
            
            p.setNombre(txtnombre.getText());
            p.setApellido(txtApellido.getText());
            p.setDireccion(txtDireccion.getText());
            p.setUsuario(txtuser.getText());
            p.setRol("Portero");
            p.setPassword(txtPass.getText());
            
            if(admin._mantenimientoPersona.actualizarPersona(Id,p))
            { 
            JOptionPane.showMessageDialog(null,"Portero Actualizado Exitosamente");
            this.setVisible(false);
            user.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Portero No existe"); 
                this.setVisible(false);
            user.setVisible(true);
            }
        }
        
        else if(cmbRol.getSelectedIndex()==1)
        {
             Administrador p = new Administrador();
            admin._mantenimientoPersona = new Modelo.mantenimientoAdministrador();
            
            p.setNombre(txtnombre.getText());
            p.setApellido(txtApellido.getText());
            p.setDireccion(txtDireccion.getText());
            p.setUsuario(txtuser.getText());
            p.setRol("Adminitrador");
            p.setPassword(txtPass.getPassword().toString());
            
            if(admin._mantenimientoPersona.actualizarPersona(Id,p))
            {
            JOptionPane.showMessageDialog(null,"Administrador Actualizado Exitosamente");
            
            
            this.setVisible(false);
            user.setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Administrador No existe");
            this.setVisible(false);
            user.setVisible(true);
            }
            
                    
        }
        
        
        
    }//GEN-LAST:event_lblguardar2MouseClicked

    private void cmbuser_update1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbuser_update1ItemStateChanged
        
        
        try
        {
          Statement stm = Modelo.Conexion.conectar().createStatement();
          ResultSet rs = stm.executeQuery("Select NombrePersona,Apellido,Descripcion_direccion,Rol,Usuario,Contraseña\n" +
"From Personas\n" +
"jOIN \n" +
"Usuarios ON Personas.IdPersonas=Usuarios.IdPersona\n" +
"WHERE Usuario='"+cmbuser_update1.getSelectedItem().toString()+"'");
          
          while(rs.next())
          {
              System.out.print(rs.getNString("NombrePersona"));
              txtnombre.setText(rs.getNString("NombrePersona"));
              txtApellido.setText(rs.getNString("Apellido"));
              txtDireccion.setText(rs.getNString("Descripcion_direccion"));
              if(rs.getString("Rol").equals("Portero"))
              {
                  cmbRol.setSelectedIndex(0);
              }
              else
              {
                  cmbRol.setSelectedIndex(1);
              }
          
              txtuser.setText(rs.getNString("Usuario"));
              txtPass.setText(rs.getNString("Contraseña"));
          }
          stm.close();
          rs.close();
        }
        catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null,ex.getMessage());  
        }
        
        
        
    }//GEN-LAST:event_cmbuser_update1ItemStateChanged

