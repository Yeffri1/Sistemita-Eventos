    private void lbl_actualizarInvitadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_actualizarInvitadoMouseClicked
       
        int Id = Integer.parseInt(txtidInvitado.getSelectedItem().toString());
        
          Administrador admin = new Administrador();
        
        admin._mantenimientoPersona = new Modelo.mantenimientoInvitado();
        
        
        Invitado i = new Invitado();
        
        i.setNombre(txtnombreInvitado.getText());
        i.setApellido(txapellidoInvitado.getText());
        i.setDireccion(txtdireccionInvitado.getText());
        i.setSexo(cmbsexoInvitado.getSelectedItem().toString());
        i.setEmail(txtemailInvitado.getText());
        Invitados user = new Invitados();
        
        if(admin._mantenimientoPersona.actualizarPersona(Id,i))
        {
        JOptionPane.showMessageDialog(null,"Invitado Actualizado");
         this.setVisible(false);
            user.setVisible(true);
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Invitado No existe");
            this.setVisible(false);
            user.setVisible(true);
        }
        
        
    }//GEN-LAST:event_lbl_actualizarInvitadoMouseClicked

