    private void txtidInvitadoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_txtidInvitadoItemStateChanged
        
        try {
            Statement stm = Conexion.conectar().createStatement();
            
            ResultSet rs = stm.executeQuery("Select * From Invitados join Personas on Invitados.IdPersona=Personas.IdPersonas where Personas.IdPersonas='"+Integer.parseInt(txtidInvitado.getSelectedItem().toString())+"'");
            
            while(rs.next())
            {
                txtnombreInvitado.setText(rs.getString("NombrePersona"));
                txapellidoInvitado.setText(rs.getString("Apellido"));
                txtdireccionInvitado.setText(rs.getString("Descripcion_direccion"));
                txtemailInvitado.setText(rs.getString("Email"));
                if(rs.getString("Sexo").equals("F"))
                  {
                     cmbsexoInvitado.setSelectedIndex(0);
                  }
                else
                {
                     cmbsexoInvitado.setSelectedIndex(1);
                }
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(frm_actualizar_invitado.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_txtidInvitadoItemStateChanged

