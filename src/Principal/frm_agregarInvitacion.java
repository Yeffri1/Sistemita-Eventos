     public static void rrellenarCmb()
    {
        try
        {
          Statement stm = Modelo.Conexion.conectar().createStatement();
          ResultSet rs = stm.executeQuery("Select * From Invitados join Personas "
                  + " on Personas.IdPersonas=Invitados.IdPersona");
          
          
          while(rs.next())
          {
              cmbInvitado.addItem(rs.getString("NombrePersona"));
              
          }
          ResultSet rs1 = stm.executeQuery("Select * From EventosProximos");
          while(rs1.next())
          {
              cmbEvento.addItem(rs1.getString("Evento"));
          }
          stm.close();
          rs.close();
          rs1.close();
        }
        catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null,ex.getMessage());  
        }
    }

