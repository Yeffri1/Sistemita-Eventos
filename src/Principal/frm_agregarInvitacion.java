    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        
        
        Administrador admin = new Administrador();
        
        admin._mantenimientoInvitaciones = new Modelo.mantenimientoInvitaciones();
         int idevento = 0,idinvitado = 0;
        
        Invitaciones i = new Invitaciones();
        
         try
        {
          Statement stm = Modelo.Conexion.conectar().createStatement();
          ResultSet rs = stm.executeQuery("Select IdEventos From Eventos where Nombre='"+cmbEvento.getSelectedItem().toString()+"'");
           
          while(rs.next())
          {
            idevento=rs.getInt("IdEventos");
              
          }
          ResultSet rs1 = stm.executeQuery("Select IdInvitados  From Invitados Join Personas on "
                  + "Personas.IdPersonas=Invitados.IdPersona where NombrePersona='"+cmbInvitado.getSelectedItem().toString()+"'");
         
          while(rs1.next())
          {
              idinvitado = rs1.getInt("IdInvitados");
          }
          stm.close();
          rs.close();
          rs1.close();
        }
        catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null,ex.getMessage());  
        }
        
        
        i.setIdEvento(idevento);
        i.setIdInvitado(idinvitado);
        
          
          
        Mantenimiento m = new Mantenimiento();
        if(admin._mantenimientoInvitaciones.enviarInvitacion(i))
        {
            
        JOptionPane.showMessageDialog(null,"Invitacion Enviada");
        this.setVisible(false);
        m.setVisible(true);
        
        }
        else
        {
            
        JOptionPane.showMessageDialog(null,"Error Invitacion");
          this.setVisible(false);
        m.setVisible(true);
        }
        
        
        
    }//GEN-LAST:event_jButton1MouseClicked

